pizzaorder <- function(order){
  if(order == "Pepperoni"){
    print("Your order is 10 dollars")
  } else if(order == "Sausage"){
    print("Your order is 11 dollars")
  } else if(order == "Cheese"){
    print("Your order is 12 dollars")
  } else{
    print("Sorry we don't have that one")
  }
}
